def para(text, style="Normal", bold=False, size=None, color=None, indent=None):
    """Build a <w:p> paragraph element as a string."""
    rpr_parts = []
    if bold:
        rpr_parts.append("<w:b/>")
    if size:
        rpr_parts.append(f'<w:sz w:val="{size}"/><w:szCs w:val="{size}"/>')
    if color:
        rpr_parts.append(f'<w:color w:val="{color}"/>')
    rpr = f"<w:rPr>{''.join(rpr_parts)}</w:rPr>" if rpr_parts else ""

    ppr_parts = [f'<w:pStyle w:val="{style}"/>']
    if indent:
        ppr_parts.append(f'<w:ind w:left="{indent}"/>')
    ppr = f"<w:pPr>{''.join(ppr_parts)}</w:pPr>"

    # Handle multi-line text: split on newlines
    lines = str(text).split("\n")
    runs = []
    for i, line in enumerate(lines):
        if i > 0:
            runs.append("<w:r><w:br/></w:r>")
        runs.append(f"<w:r>{rpr}<w:t xml:space=\"preserve\">{xe(line)}</w:t></w:r>")

    return f"<w:p>{ppr}{''.join(runs)}</w:p>"


def heading1(text):
    return para(text, style="Heading1")


def heading2(text):
    return para(text, style="Heading2")


def heading3(text):
    return para(text, style="Heading3")


def label_value(label, value):
    """Bold label followed by normal value in same paragraph."""
    rpr_bold = "<w:rPr><w:b/></w:rPr>"
    rpr_norm = "<w:rPr></w:rPr>"
    ppr = '<w:pPr><w:pStyle w:val="Normal"/></w:pPr>'
    label_run = f'<w:r>{rpr_bold}<w:t xml:space="preserve">{xe(label)}</w:t></w:r>'
    value_run = f'<w:r>{rpr_norm}<w:t xml:space="preserve">{xe(value)}</w:t></w:r>'
    return f"<w:p>{ppr}{label_run}{value_run}</w:p>"


def bullet(text):
    """A bulleted list item using ListParagraph style."""
    ppr = (
        '<w:pPr>'
        '<w:pStyle w:val="ListParagraph"/>'
        '<w:numPr><w:ilvl w:val="0"/><w:numId w:val="1"/></w:numPr>'
        '</w:pPr>'
    )
    return f'<w:p>{ppr}<w:r><w:t xml:space="preserve">{xe(text)}</w:t></w:r></w:p>'


def blank():
    return '<w:p><w:pPr><w:pStyle w:val="Normal"/></w:pPr></w:p>'


def disclaimer_para(text):
    """Highlighted disclaimer paragraph."""
    ppr = '<w:pPr><w:pStyle w:val="Normal"/><w:shd w:val="clear" w:color="auto" w:fill="FFF2CC"/></w:pPr>'
    rpr = "<w:rPr><w:b/><w:color w:val=\"7F6000\"/></w:rPr>"
    return (
        f'<w:p>{ppr}'
        f'<w:r>{rpr}<w:t xml:space="preserve">{xe(text)}</w:t></w:r>'
        f'</w:p>'
    )


def horizontal_rule():
    """Paragraph with a bottom border acting as a divider."""
    return (
        '<w:p>'
        '<w:pPr>'
        '<w:pBdr>'
        '<w:bottom w:val="single" w:sz="6" w:space="1" w:color="4472C4"/>'
        '</w:pBdr>'
        '</w:pPr>'
        '</w:p>'
    )


