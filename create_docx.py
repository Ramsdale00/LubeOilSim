# Build document body
# ---------------------------------------------------------------------------
def build_body():
    parts = []

    # Cover heading
    parts.append(
        '<w:p>'
        '<w:pPr><w:pStyle w:val="Title"/></w:pPr>'
        '<w:r><w:t>OmniBlend Simulator</w:t></w:r>'
        '</w:p>'
    )
    parts.append(
        '<w:p>'
        '<w:pPr><w:pStyle w:val="Subtitle"/></w:pPr>'
        '<w:r><w:t>Module Documentation</w:t></w:r>'
        '</w:p>'
    )
    parts.append(blank())
    parts.append(disclaimer_para(DISCLAIMER))
    parts.append(blank())
    parts.append(horizontal_rule())
    parts.append(blank())

    # Modules
    for m in MODULES:
        parts.append(heading1(f"Module {m['num']}: {m['name']}"))
        parts.append(label_value("Status: ", m["status"]))
        parts.append(blank())

        parts.append(heading2("Function"))
        parts.append(para(m["function"]))
        parts.append(blank())

        parts.append(heading2("Current State"))
        parts.append(para(m["current_state"]))
        parts.append(blank())

        parts.append(heading2("Requirements to Go Live"))
        for req in m["reqs"]:
            parts.append(bullet(req))
        parts.append(blank())

        parts.append(heading2("Timeline Phases"))
        parts.append(para(m["phases"]))
        parts.append(blank())
        parts.append(horizontal_rule())
        parts.append(blank())

    # Timeline section
    parts.append(heading1("Tentative Implementation Timeline"))
    parts.append(disclaimer_para(DISCLAIMER))
    parts.append(blank())

    for p in PHASES:
        parts.append(heading2(f"{p['phase']}: {p['name']}"))
        parts.append(label_value("Estimated Weeks: ", p["weeks"]))
        parts.append(label_value("Modules Affected: ", p["modules"]))
        parts.append(blank())
        parts.append(heading3("Key Work"))
        parts.append(para(p["key_work"]))
        parts.append(blank())

    return "".join(parts)


