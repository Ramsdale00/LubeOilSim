# ---------------------------------------------------------------------------
# Helper: escape XML special chars
# ---------------------------------------------------------------------------
def xe(s):
    return (str(s)
            .replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;")
            .replace("'", "&apos;"))

