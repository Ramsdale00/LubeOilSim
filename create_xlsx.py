# ---------------------------------------------------------------------------
# Shared styles helper (inline styles embedded in XML)
# ---------------------------------------------------------------------------
# We use a simple shared-string table + styles approach.

def build_workbook():
    """
    Returns a dict of {filename: bytes} representing the xlsx archive.
    """
    files = {}

    # -----------------------------------------------------------------------
    # Collect all unique strings for the shared strings table
    # -----------------------------------------------------------------------
    all_strings = []
    string_index = {}

    def si(s):
        """Get or create shared-string index."""
        s = str(s)
        if s not in string_index:
            string_index[s] = len(all_strings)
            all_strings.append(s)
        return string_index[s]

    # -----------------------------------------------------------------------
    # Sheet 1: Module Overview
    # -----------------------------------------------------------------------
    sheet1_rows = []
    # Disclaimer row
    sheet1_rows.append([(si(DISCLAIMER), "s")])
    sheet1_rows.append([])  # blank
    # Header
    sheet1_rows.append([
        (si("Module #"), "s"),
        (si("Module Name"), "s"),
        (si("Status"), "s"),
        (si("Function / Description"), "s"),
        (si("Current State"), "s"),
        (si("Timeline Phases"), "s"),
    ])
    for m in MODULES:
        sheet1_rows.append([
            (si(m["num"]), "n"),
            (si(m["name"]), "s"),
            (si(m["status"]), "s"),
            (si(m["function"]), "s"),
            (si(m["current_state"]), "s"),
            (si(m["phases"]), "s"),
        ])

    # -----------------------------------------------------------------------
    # Sheet 2: Requirements to Go Live
    # -----------------------------------------------------------------------
    sheet2_rows = []
    sheet2_rows.append([(si(DISCLAIMER), "s")])
    sheet2_rows.append([])
    sheet2_rows.append([
        (si("Module #"), "s"),
        (si("Module Name"), "s"),
        (si("Requirement"), "s"),
    ])
    for m in MODULES:
        for req in m["reqs"]:
            sheet2_rows.append([
                (si(m["num"]), "n"),
                (si(m["name"]), "s"),
                (si(req), "s"),
            ])

    # -----------------------------------------------------------------------
    # Sheet 3: Tentative Timeline
    # -----------------------------------------------------------------------
    sheet3_rows = []
    sheet3_rows.append([(si(DISCLAIMER), "s")])
    sheet3_rows.append([])
    sheet3_rows.append([
        (si("Phase"), "s"),
        (si("Phase Name"), "s"),
        (si("Estimated Weeks"), "s"),
        (si("Modules Affected"), "s"),
        (si("Key Work"), "s"),
    ])
    for p in PHASES:
        sheet3_rows.append([
            (si(p["phase"]), "s"),
            (si(p["name"]), "s"),
            (si(p["weeks"]), "s"),
            (si(p["modules"]), "s"),
            (si(p["key_work"]), "s"),
        ])

    # -----------------------------------------------------------------------
    # Build shared strings XML
    # -----------------------------------------------------------------------
    ss_items = "".join(
        f"<si><t xml:space=\"preserve\">{xe(s)}</t></si>" for s in all_strings
    )
    shared_strings_xml = (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        f'<sst xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main" '
        f'count="{len(all_strings)}" uniqueCount="{len(all_strings)}">'
        f'{ss_items}'
        f'</sst>'
    )
    files["xl/sharedStrings.xml"] = shared_strings_xml.encode("utf-8")

    # -----------------------------------------------------------------------
    # Build styles XML (minimal)
    # -----------------------------------------------------------------------
    # Font 0 = normal, Font 1 = bold
    # Fill 0 = none, Fill 1 = gray125 (required), Fill 2 = header blue
    # Border 0 = none
    # CellXf 0 = normal, 1 = bold, 2 = bold+blue fill, 3 = wrap
    styles_xml = (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        '<styleSheet xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main">'
        '<fonts count="3">'
        '<font><sz val="10"/><name val="Calibri"/></font>'
        '<font><b/><sz val="10"/><name val="Calibri"/></font>'
        '<font><b/><sz val="10"/><name val="Calibri"/><color rgb="FFFFFFFF"/></font>'
        '</fonts>'
        '<fills count="4">'
        '<fill><patternFill patternType="none"/></fill>'
        '<fill><patternFill patternType="gray125"/></fill>'
        '<fill><patternFill patternType="solid"><fgColor rgb="FF2F5496"/></patternFill></fill>'
        '<fill><patternFill patternType="solid"><fgColor rgb="FFFFE699"/></patternFill></fill>'
        '</fills>'
        '<borders count="2">'
        '<border><left/><right/><top/><bottom/><diagonal/></border>'
        '<border>'
        '<left style="thin"><color rgb="FF9E9E9E"/></left>'
        '<right style="thin"><color rgb="FF9E9E9E"/></right>'
        '<top style="thin"><color rgb="FF9E9E9E"/></top>'
        '<bottom style="thin"><color rgb="FF9E9E9E"/></bottom>'
        '<diagonal/>'
        '</border>'
        '</borders>'
        '<cellStyleXfs count="1"><xf numFmtId="0" fontId="0" fillId="0" borderId="0"/></cellStyleXfs>'
        '<cellXfs count="6">'
        # 0 = normal
        '<xf numFmtId="0" fontId="0" fillId="0" borderId="0" xfId="0">'
        '<alignment wrapText="1" vertical="top"/></xf>'
        # 1 = bold header (white on blue)
        '<xf numFmtId="0" fontId="2" fillId="2" borderId="1" xfId="0">'
        '<alignment wrapText="1" vertical="center" horizontal="center"/></xf>'
        # 2 = disclaimer (yellow fill, bold)
        '<xf numFmtId="0" fontId="1" fillId="3" borderId="1" xfId="0">'
        '<alignment wrapText="1" vertical="top"/></xf>'
        # 3 = normal with border + wrap
        '<xf numFmtId="0" fontId="0" fillId="0" borderId="1" xfId="0">'
        '<alignment wrapText="1" vertical="top"/></xf>'
        # 4 = bold with border (for module name)
        '<xf numFmtId="0" fontId="1" fillId="0" borderId="1" xfId="0">'
        '<alignment wrapText="1" vertical="top"/></xf>'
        # 5 = centered with border
        '<xf numFmtId="0" fontId="0" fillId="0" borderId="1" xfId="0">'
        '<alignment wrapText="1" vertical="top" horizontal="center"/></xf>'
        '</cellXfs>'
        '</styleSheet>'
    )
    files["xl/styles.xml"] = styles_xml.encode("utf-8")

    # -----------------------------------------------------------------------
    # Helper: build a sheet XML from rows
    # -----------------------------------------------------------------------
    COL_LETTERS = ["A", "B", "C", "D", "E", "F", "G", "H"]

    def build_sheet_xml(rows, col_widths=None, header_row=3):
        """header_row is 1-based row index of the header (for styling)."""
        xml_rows = []
        for row_idx, row in enumerate(rows, start=1):
            if not row:
                xml_rows.append(f'<row r="{row_idx}"/>')
                continue
            cells = []
            for col_idx, cell in enumerate(row):
                col_letter = COL_LETTERS[col_idx]
                ref = f"{col_letter}{row_idx}"
                val, typ = cell

                # Determine style
                if row_idx == 1:
                    # disclaimer
                    style = ' s="2"'
                elif row_idx == header_row:
                    style = ' s="1"'
                elif typ == "n":
                    style = ' s="5"'
                elif col_idx == 1 and row_idx > header_row:
                    # module name column - bold
                    style = ' s="4"'
                else:
                    style = ' s="3"'

                if typ == "s":
                    cells.append(
                        f'<c r="{ref}" t="s"{style}><v>{val}</v></c>'
                    )
                else:
                    cells.append(
                        f'<c r="{ref}"{style}><v>{val}</v></c>'
                    )
            xml_rows.append(
                f'<row r="{row_idx}">' + "".join(cells) + "</row>"
            )

        cols_xml = ""
        if col_widths:
            col_els = "".join(
                f'<col min="{i+1}" max="{i+1}" width="{w}" customWidth="1"/>'
                for i, w in enumerate(col_widths)
            )
            cols_xml = f"<cols>{col_els}</cols>"

        return (
            '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
            '<worksheet xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main"'
            ' xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships">'
            f'{cols_xml}'
            '<sheetData>'
            + "".join(xml_rows) +
            '</sheetData>'
            '</worksheet>'
        )

    files["xl/worksheets/sheet1.xml"] = build_sheet_xml(
        sheet1_rows,
        col_widths=[8, 22, 18, 60, 50, 40],
        header_row=3,
    ).encode("utf-8")

    files["xl/worksheets/sheet2.xml"] = build_sheet_xml(
        sheet2_rows,
        col_widths=[8, 22, 80],
        header_row=3,
    ).encode("utf-8")

    files["xl/worksheets/sheet3.xml"] = build_sheet_xml(
        sheet3_rows,
        col_widths=[10, 30, 28, 45, 70],
        header_row=3,
    ).encode("utf-8")

    # -----------------------------------------------------------------------
    # workbook.xml
    # -----------------------------------------------------------------------
    workbook_xml = (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        '<workbook xmlns="http://schemas.openxmlformats.org/spreadsheetml/2006/main"'
        ' xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships">'
        '<sheets>'
        '<sheet name="Module Overview" sheetId="1" r:id="rId1"/>'
        '<sheet name="Requirements to Go Live" sheetId="2" r:id="rId2"/>'
        '<sheet name="Tentative Timeline" sheetId="3" r:id="rId3"/>'
        '</sheets>'
        '</workbook>'
    )
    files["xl/workbook.xml"] = workbook_xml.encode("utf-8")

    # -----------------------------------------------------------------------
    # Relationships
    # -----------------------------------------------------------------------
    workbook_rels = (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">'
        '<Relationship Id="rId1" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/worksheet" '
        'Target="worksheets/sheet1.xml"/>'
        '<Relationship Id="rId2" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/worksheet" '
        'Target="worksheets/sheet2.xml"/>'
        '<Relationship Id="rId3" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/worksheet" '
        'Target="worksheets/sheet3.xml"/>'
        '<Relationship Id="rId4" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/sharedStrings" '
        'Target="sharedStrings.xml"/>'
        '<Relationship Id="rId5" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/styles" '
        'Target="styles.xml"/>'
        '</Relationships>'
    )
    files["xl/_rels/workbook.xml.rels"] = workbook_rels.encode("utf-8")

    root_rels = (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">'
        '<Relationship Id="rId1" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" '
        'Target="xl/workbook.xml"/>'
        '</Relationships>'
    )
    files["_rels/.rels"] = root_rels.encode("utf-8")

    # -----------------------------------------------------------------------
    # [Content_Types].xml
    # -----------------------------------------------------------------------
    content_types = (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        '<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">'
        '<Default Extension="rels" '
        'ContentType="application/vnd.openxmlformats-package.relationships+xml"/>'
        '<Default Extension="xml" ContentType="application/xml"/>'
        '<Override PartName="/xl/workbook.xml" '
        'ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet.main+xml"/>'
        '<Override PartName="/xl/worksheets/sheet1.xml" '
        'ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.worksheet+xml"/>'
        '<Override PartName="/xl/worksheets/sheet2.xml" '
        'ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.worksheet+xml"/>'
        '<Override PartName="/xl/worksheets/sheet3.xml" '
        'ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.worksheet+xml"/>'
        '<Override PartName="/xl/sharedStrings.xml" '
        'ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.sharedStrings+xml"/>'
        '<Override PartName="/xl/styles.xml" '
        'ContentType="application/vnd.openxmlformats-officedocument.spreadsheetml.styles+xml"/>'
        '</Types>'
    )
    files["[Content_Types].xml"] = content_types.encode("utf-8")

    return files


def main():
    files = build_workbook()
    with zipfile.ZipFile(OUTPUT_PATH, "w", zipfile.ZIP_DEFLATED) as zf:
        for name, data in files.items():
            zf.writestr(name, data)
    size = os.path.getsize(OUTPUT_PATH)
    print(f"Created: {OUTPUT_PATH} ({size:,} bytes)")


if __name__ == "__main__":
    main()
